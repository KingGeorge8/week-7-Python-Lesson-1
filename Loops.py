#Loops

fruits = ['apple', 'banana', 'cherry']

for fruit in fruits:
    print(fruit)
    
    
    numbers = [1, 2, 3, 5, 7]
    
    for number in numbers:
        print(number)
        
    count = 1
    
    #using a while loop to count from 1 to 5
    while count <= 5:
        print(count)
        count += 1 #increment the count by 1